    private void comparaEmail(String email, String confirmaEmail)
            throws Exception {
        if (!email.equals(confirmaEmail)) {
            throw new Exception("E-mails n√£o conferem.");
        }
    }

    private static String criptografar(String pass) throws NoSuchAlgorithmException {
        MessageDigest m = MessageDigest.getInstance("MD5");
        byte[] data = pass.getBytes();
        m.update(data, 0, data.length);
        BigInteger i = new BigInteger(1, m.digest());
        return String.format("%1$032X", i);
    }

    private void testaSenha(String novaSenha) throws Exception {
        novaSenha = criptografar(novaSenha);
        if (!novaSenha.equals(Sistema.instance.getUsuarioLogado().getSenha())) {
            throw new Exception("Senha errada.");
        }
    }

