    private void comparaSenha(String senha, String confirmaSenha)
            throws Exception {
        if (!senha.equals(confirmaSenha)) {
            throw new Exception("Senhas não conferem.");
        }
    }
    
    private String getDica() {
        return inputDica.getText();
    }

