    private void editarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarButtonActionPerformed
        try{
            if (getSenha().isEmpty() && getConfirmaSenha().isEmpty()) {
                Sistema.instance.editaUsuario(getNome(), null, getDica());
            } else {
                comparaSenha(getSenha(), getConfirmaSenha());
                Sistema.instance.editaUsuario(getNome(), getSenha(), getDica());
            }
            JOptionPane.showMessageDialog(rootPane, "Perfil editado com sucesso.");
            dispose();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_editarButtonActionPerformed

