    private void botaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoEditarActionPerformed
        try {
            if (conta != null) {
            Sistema.instance.editaConta(conta.getNome(), getNovoNomeDaConta());
            JOptionPane.showMessageDialog(rootPane, "Conta editada com sucesso.");
            dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Não há conta selecionada.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_botaoEditarActionPerformed

