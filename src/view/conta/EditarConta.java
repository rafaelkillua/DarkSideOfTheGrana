    private void selecionarContaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selecionarContaActionPerformed
        try {
            conta = Sistema.instance.pesquisaConta(getNomeDaConta());
            inputNome.setText(conta.getNome());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_selecionarContaActionPerformed

    public String getNomeDaConta() throws Exception {
        if (selecionarConta.getSelectedItem() == null) {
            throw new Exception ("Não ha conta selecionada.");
        }
        return (String) selecionarConta.getSelectedItem();
    }

    private String getNovoNomeDaConta() {
        return inputNome.getText();
    }

    public DefaultComboBoxModel listaDeConta(List<Conta> listaDeContas) {
        ArrayList<String> strings = new ArrayList<>();
        for (Iterator<Conta> it = listaDeContas.iterator(); it.hasNext();) {
            Conta cadaConta = it.next();
            strings.add(cadaConta.getNome());
        }
        DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();
        for (String string : strings) {
            model.addElement(string);
        }
        return model;
    }

